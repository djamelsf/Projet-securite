     public static BigInteger pgcd(BigInteger a,BigInteger b) {
    
    while (!a.equals(b))  if (a.compareTo(b)==1){a=a.subtract(b);}else {b=b.subtract(a);};
    return a;
    }
    
    
    static BigInteger GenerateE(BigInteger phi){
        BigInteger e;
        do{
        BigInteger a=BBSparm(20);
        //System.out.println(a);
        String b=BBS(a, new BigInteger("5"),20);
        //System.out.println(b);
        e=new BigInteger(b, 2);
            
        }while(!pgcd(phi, e).equals(BigInteger.ONE));
        return e;     
    }
    
    public static BigIntEuclidean calculate(BigInteger a, BigInteger b)
    {
        BigInteger x = BigInteger.ZERO; // BigInteger x= new bigInteger("0");
        BigInteger lastx = BigInteger.ONE;
        BigInteger y = BigInteger.ONE;
        BigInteger lasty = BigInteger.ZERO;
        while (!b.equals(BigInteger.ZERO))
        {
            BigInteger[] quotientAndRemainder = a.divideAndRemainder(b);
            BigInteger quotient = quotientAndRemainder[0];

            BigInteger temp = a;
            a = b;
            b = quotientAndRemainder[1];

            temp = x;
            x = lastx.subtract(quotient.multiply(x));
            lastx = temp;

            temp = y;
            y = lasty.subtract(quotient.multiply(y));
            lasty = temp;
        }

        BigIntEuclidean result = new BigIntEuclidean();
        result.x = lastx;
        result.y = lasty;
        result.gcd = a;
        return result;
    }
    public static BigInteger generateD(BigInteger e,BigInteger phi){
        BigInteger d=calculate(e, phi).x;
        if(d.compareTo(new BigInteger("0"))==-1){
            d=d.add(phi);
            
        }
        return d;
        
    }
    public static BigInteger[] Crypter(String msg,BigInteger e,BigInteger n){
        //le vercteur v est supposé =9
        msg="k"+msg;
        int v=8;
        char c;
        //tab c'est le tableau qui va contenir les valeurs chifrées
        BigInteger[] tab=new BigInteger[msg.length()];
        //m c'est la converstion de la valeur xor en bigInteger
        BigInteger m;
        c=msg.charAt(0);
        int xor=v^c;
        m=new BigInteger(xor+"");
        //donc chaque element du tableau tab va recevoir m.modpow(e,n)
        tab[0]=m.modPow(e, n);
        for (int i = 1; i < msg.length(); i++) {
            //meme travail juste qu'ici on fait le xor avec l'element precedent 
            c=msg.charAt(i);
            xor=xor^c;
            m=new BigInteger(xor+"");
            tab[i]=m.modPow(e, n);  
        }
        
        return tab;
    }
    public static String Decrypter(BigInteger[] cry,BigInteger d,BigInteger n){
        int v=8;
        BigInteger ci;
        int c;
        String m="";
        ci=cry[0].modPow(d, n);
        c=Integer.parseInt(ci+"");
        char mi=(char) (v^c);
        m=m+mi;
        for (int i = 1; i < cry.length; i++) {
           ci=cry[i].modPow(d, n);
           c=Integer.parseInt(ci+"");
           
           BigInteger ci2=cry[i-1].modPow(d, n);
           int c2=Integer.parseInt(ci2+"");
           
           
           mi=(char) (c2^c);
           m=m+mi;
            
            
        }
        return m;
    }
