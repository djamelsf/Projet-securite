static BigInteger BBSparm(int k){
            
            BigInteger p;
            BigInteger q;
            do{
            SecureRandom sc=new SecureRandom();
            BigInteger alpha=new BigInteger(k-2, sc);
            p=alpha.multiply(new BigInteger("3")).add(new BigInteger("4"));
            }while(!isPrime(p));
            do{
            SecureRandom sc=new SecureRandom();
            BigInteger alpha=new BigInteger(k-2,sc);
            q=alpha.multiply(new BigInteger("3")).add(new BigInteger("4"));
            }while(!isPrime(q));
            return p.multiply(q);
            
            
            
            
    }
   
    
    static String BBS(BigInteger n,BigInteger s,int l){
        String m="";
        BigInteger x=s.modPow(new BigInteger("2"), n);
       
        
        for (int i = 1; i <= l; i++) {
            x=x.modPow(new BigInteger("2"), n);
            
            m=x.mod(new BigInteger("2"))+m;
        }
        return m;
    }
    
    
    public static boolean isPrime(BigInteger a) {
    
    if(a.equals(new BigInteger("1"))){
            return false;
        }else{
            BigInteger i=new BigInteger("2");
            while (i.compareTo(a.subtract(new BigInteger("1")))==-1) {
                if(a.mod(i).equals(new BigInteger("0"))){
                    return false;
                }
                i=i.add(new BigInteger("1"));
            }
            return true;
        }
}
